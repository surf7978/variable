package variable4;

public class Member {
	private int id;
	private String name;
	private String ph;
	private String cls;

	public void setMemberNo(int id) {
		this.id = id;
	}

	public void setMemberName(String name) {
		this.name = name;
	}

	public void setMemberPh(String ph) {
		this.ph = ph;
	}

	public void setMemberCls(String cls) {
		this.cls = cls;
	}
}
